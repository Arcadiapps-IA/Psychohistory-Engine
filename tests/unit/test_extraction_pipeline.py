def test_extraction_pipeline_run_returns_report() -> None:
    """ExtractionPipeline.run() returns a valid ExtractionReport."""
    ingester = EventIngester()
    pipeline = ExtractionPipeline(event_ingester=ingester)

    docs = [
        _make_raw_doc(
            raw_content="In 1789 the French Revolution began in Paris.",
            source_url="https://example.com/french-revolution",
        ),
        _make_raw_doc(
            raw_content="In 1815 the Battle of Waterloo ended the Napoleonic Wars.",
            source_url="https://example.com/waterloo",
        ),
    ]
    connector = _make_mock_connector(docs=docs)

    query = SearchQuery(text="history")
    report = pipeline.run(connector, query)

    assert isinstance(report, ExtractionReport)
    assert report.connector_name == "mock_connector"
    assert report.documents_retrieved == 2
    assert report.events_generated >= 0
    assert isinstance(report.executed_at, datetime)


def test_extraction_pipeline_discards_documents_without_required_fields() -> None:
    """Documents without a year in content are discarded."""
    ingester = EventIngester()
    pipeline = ExtractionPipeline(event_ingester=ingester)

    # Document with no year — should be discarded
    docs = [
        _make_raw_doc(
            raw_content="No date information here at all.",
            source_url="https://example.com/no-date",
        ),
    ]
    connector = _make_mock_connector(docs=docs)

    query = SearchQuery(text="test")
    report = pipeline.run(connector, query)

    assert report.documents_discarded >= 1
    assert report.events_generated == 0


def test_extraction_pipeline_run_updates_checkpoint() -> None:
    """After run(), the checkpoint for the connector is updated."""
    ingester = EventIngester()
    pipeline = ExtractionPipeline(event_ingester=ingester)

    connector = _make_mock_connector(connector_name="wiki", docs=[])
    query = SearchQuery(text="test")

    assert "wiki" not in pipeline._last_extraction

    pipeline.run(connector, query)

    assert "wiki" in pipeline._last_extraction
    assert isinstance(pipeline._last_extraction["wiki"], datetime)


def test_extraction_pipeline_deduplicates_within_batch() -> None:
    """Duplicate documents in the same batch produce only one event."""
    ingester = EventIngester()
    pipeline = ExtractionPipeline(event_ingester=ingester)

    # Two identical documents
    content = "In 1789 the French Revolution began."
    docs = [
        _make_raw_doc(raw_content=content, source_url="https://example.com/a"),
        _make_raw_doc(raw_content=content, source_url="https://example.com/b"),
    ]
    connector = _make_mock_connector(docs=docs)

    query = SearchQuery(text="test")
    report = pipeline.run(connector, query)

    # Both docs retrieved, but only 1 unique event
    assert report.documents_retrieved == 2
    assert report.events_generated == 1


def test_extraction_pipeline_transform_extracts_year() -> None:
    """_transform() correctly extracts a year from content."""
    ingester = EventIngester()

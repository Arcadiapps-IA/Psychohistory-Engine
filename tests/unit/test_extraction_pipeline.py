def test_wikipedia_connector_returns_empty_on_network_error() -> None:
    """WikipediaConnector.search() returns [] when requests raises a timeout."""
    connector = WikipediaConnector()
    query = SearchQuery(text="French Revolution")

    with patch("psychohistory.connectors.wikipedia._get_requests") as mock_get_req:
        import requests as req_module
        mock_requests = MagicMock()
        mock_requests.get.side_effect = req_module.exceptions.Timeout("timed out")
        mock_get_req.return_value = mock_requests

        result = connector.search(query)

    assert result == [], "Should return empty list on timeout"



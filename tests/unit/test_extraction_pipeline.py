def test_extraction_pipeline_transform_sets_attribution_metadata() -> None:
    """_transform() sets source_url, connector_name, extraction_timestamp."""
    ingester = EventIngester()
    pipeline = ExtractionPipeline(event_ingester=ingester)

    ts = datetime(2023, 6, 15, tzinfo=timezone.utc)
    doc = _make_raw_doc(
        raw_content="In 1789 the French Revolution began.",
        connector_name="wikipedia",
        source_url="https://en.wikipedia.org/wiki/French_Revolution",
        extraction_timestamp=ts,
    )
    events = pipeline._transform(doc)

    assert len(events) == 1
    event = events[0]
    assert event.source_url == "https://en.wikipedia.org/wiki/French_Revolution"
    assert event.connector_name == "wikipedia"
    assert event.extraction_timestamp == ts

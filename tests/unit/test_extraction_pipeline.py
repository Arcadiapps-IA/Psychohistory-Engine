# ---------------------------------------------------------------------------
# Helpers / factories
# ---------------------------------------------------------------------------


def _make_raw_doc(
    raw_content: str = "In 1789 the French Revolution began.",
    connector_name: str = "test_connector",
    source_url: str = "https://example.com/doc",
    extraction_timestamp: datetime | None = None,
) -> RawSourceDocument:
    return RawSourceDocument(
        id=str(uuid.uuid4()),
        source_url=source_url,
        connector_name=connector_name,
        raw_content=raw_content,
        content_type="text/plain",
        extraction_timestamp=extraction_timestamp or datetime.now(timezone.utc),
        metadata={},
    )


def _make_mock_connector(
    connector_name: str = "mock_connector",
    docs: list[RawSourceDocument] | None = None,
) -> MagicMock:
    connector = MagicMock()
    connector.connector_name = connector_name
    connector.search.return_value = docs or []
    return connector



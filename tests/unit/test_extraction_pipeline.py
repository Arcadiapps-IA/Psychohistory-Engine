@given(raw_content=st.text(min_size=1, max_size=200))
@settings(max_examples=50)
def test_raw_document_preserved(raw_content: str) -> None:
    """
    **Validates: Requirements 10.3**

    Property 21: The RawSourceDocument stored must have raw_content identical
    to the original content.
    """
    doc = _make_raw_doc(raw_content=raw_content)
    # The raw_content must be preserved exactly as provided
    assert doc.raw_content == raw_content


# ---------------------------------------------------------------------------
# Property 22: Attribution metadata completeness
# **Validates: Requirements 10.4, 10.6**
# ---------------------------------------------------------------------------


@given(
    raw_content=st.text(min_size=10, max_size=200).filter(
        lambda s: any(c.isdigit() for c in s)
    ),
    connector_name=st.text(min_size=1, max_size=50, alphabet=st.characters(whitelist_categories=("Lu", "Ll", "Nd"), whitelist_characters="_-")),
    source_url=st.just("https://example.com/article"),
)
@settings(max_examples=50)
def test_attribution_metadata_complete(
    raw_content: str, connector_name: str, source_url: str
) -> None:
    """
    **Validates: Requirements 10.4, 10.6**

    Property 22: For any HistoricalEvent generated by the ExtractionPipeline,
    source_url, connector_name, and extraction_timestamp must be non-null.
    """
    ingester = EventIngester()
    pipeline = ExtractionPipeline(event_ingester=ingester)

    # Build a doc with a year in the content to ensure transformation succeeds
    content_with_year = raw_content
    if not any(str(y) in raw_content for y in range(1000, 2025)):
        content_with_year = f"In 1900 {raw_content}"

    doc = RawSourceDocument(
        id=str(uuid.uuid4()),
        source_url=source_url,
        connector_name=connector_name,
        raw_content=content_with_year,
        content_type="text/plain",
        extraction_timestamp=datetime.now(timezone.utc),
        metadata={},
    )

    events = pipeline._transform(doc)
    for event in events:
        assert event.source_url is not None, "source_url must not be None"
        assert event.connector_name is not None, "connector_name must not be None"
        assert event.extraction_timestamp is not None, "extraction_timestamp must not be None"


# ---------------------------------------------------------------------------
# Property 23: Incremental deduplication
# **Validates: Requirements 10.11**
# ---------------------------------------------------------------------------


def test_incremental_extraction_no_duplicates() -> None:
    """
    **Validates: Requirements 10.11**

    Property 23: Documents with extraction_timestamp before the checkpoint
    must not be ingested.
    """
    ingester = EventIngester()
    pipeline = ExtractionPipeline(event_ingester=ingester)

    checkpoint_time = datetime(2023, 6, 1, tzinfo=timezone.utc)
    connector_name = "test_connector"

    # Set checkpoint
    pipeline._last_extraction[connector_name] = checkpoint_time

    # Document BEFORE checkpoint — should be excluded
    old_doc = _make_raw_doc(
        raw_content="In 1789 the French Revolution began.",
        connector_name=connector_name,
        extraction_timestamp=datetime(2023, 1, 1, tzinfo=timezone.utc),
    )

    # Document AFTER checkpoint — should be included
    new_doc = _make_raw_doc(
        raw_content="In 1815 the Battle of Waterloo ended the Napoleonic Wars.",
        connector_name=connector_name,
        extraction_timestamp=datetime(2023, 12, 1, tzinfo=timezone.utc),
    )

    assert not pipeline._is_incremental_eligible(old_doc, connector_name), (
        "Old document should not be eligible"
    )
    assert pipeline._is_incremental_eligible(new_doc, connector_name), (
        "New document should be eligible"
    )


def test_incremental_no_checkpoint_always_eligible() -> None:
    """Without a checkpoint, all documents are eligible."""
    ingester = EventIngester()
    pipeline = ExtractionPipeline(event_ingester=ingester)

    doc = _make_raw_doc(
        extraction_timestamp=datetime(2000, 1, 1, tzinfo=timezone.utc),
    )
    assert pipeline._is_incremental_eligible(doc, "any_connector")


